OZON_ALGORITHM_VERSION = "2026.1"

OZON_RULES = """
## Правила Ozon (версия {version})

### Заголовок (ozon_title)
- Длина: до 150 символов с пробелами
- Структура: [Тип товара] + [Бренд] + [Модель/Артикул] + [Ключевые характеристики через запятую]
- Максимально информативно: заголовок — это сниппет в поисковой выдаче
- Без CAPS LOCK, без стоп-слов маркетплейса
- Без дублирования слов и переспама
- Пример: "Умная колонка Яндекс Станция Мини 2, с Алисой, синий, YNDX-0002"
- НЕ используй решётки (#), только текст

### Описание (ozon_description)
- Structured data формат: сухой, фактологический, SEO-плотный
- Используй Markdown-разметку для структуры (заголовки ##, списки -)
- Тон: информационный, без эмоций и лишних прилагательных
- Структура:
  1. ## Описание — 2-3 предложения о товаре
  2. ## Характеристики — маркированный список ключевых параметров
  3. ## Комплектация — что входит в комплект
  4. ## Особенности — технологические или дизайнерские детали
  5. ## Условия эксплуатации и уход — рекомендации
- Плотность ключевых слов: каждое второе предложение содержит SEO-ключ
- Без эмодзи
- Без ссылок и контактов

### Ключевые слова (ozon_keywords)
- Ровно 25 SEO-ключей
- Включай: прямые входы, синонимы, опечатки, латиницу
- Английские SEO-ключи на языке покупателя (например: "cat bed", "yoga mat", "face cream")
- НЕ транслитерируй русские слова латиницей (никаких "lejanka", "kovrik", "krem")
- Разделяй высокочастотные (1-2 слова) и низкочастотные (3-5 слов) ключи
- Формат: список строк ["ключ 1", "ключ 2", ...]

### Сценарий видео (ozon_video_script)
- Хронометраж: 15–60 секунд
- Формат: горизонтальное видео 16:9, Full HD
- Структура сценария:
  1. 0–3 сек: Хук (проблема или wow-эффект)
  2. 3–10 сек: Демонстрация товара (крупный план, вращение на 360°)
  3. 10–25 сек: Ключевые характеристики (текст на экране + голос)
  4. 25–45 сек: Сценарий использования (товар в деле)
  5. 45–55 сек: Преимущества перед аналогами
  6. 55–60 сек: CTA (призыв к покупке)
- Без музыки с авторскими правами
- Субтитры обязательны (30% пользователей смотрят без звука)

### Фото-рекомендации (ozon_photo_recommendations)
- 6–10 фотографий
- Фото 1: товар на белом фоне #FFFFFF, занимает 80% кадра
- Фото 2: товар с другого ракурса (сзади, сбоку)
- Фото 3: в масштабе / с предметом для сравнения размера
- Фото 4–5: детали (текстура, логотип, ключевые элементы)
- Фото 6: комплектация (всё, что в коробке)
- Фото 7–8: lifestyle / товар в интерьере
- Фото 9: инфографика с характеристиками
- Фото 10: размерная сетка (если применимо)
- Разрешение: минимум 1000×1000 px (квадрат или 3:4)
- Формат: JPEG или PNG
""".format(version=OZON_ALGORITHM_VERSION)


def get_ozon_rules() -> str:
    return OZON_RULES
