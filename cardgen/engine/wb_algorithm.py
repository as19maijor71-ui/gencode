WB_ALGORITHM_VERSION = "2026.1"

WB_RULES = """
## Правила Wildberries (версия {version})

### Заголовок (wb_title)
- Длина: 60–100 символов с пробелами
- Структура: [Тип товара] + [Бренд] + [2-3 ключевые характеристики]
- Без CAPS LOCK, без слов «купить», «цена», «скидка», «акция», «лучший», «хит», «новинка»
- Без восклицательных знаков
- CTR-оптимизация: главное конкурентное преимущество в первые 40 символов
- Пример: "Платье женское летнее BrandName хлопок свободный крой"
- НЕ используй решётки (#), только текст

### Описание (wb_description)
- Длинный продающий текст, цель — максимальный dwell-time
- 5–7 смысловых блоков с эмодзи-заголовками
- Тон: экспертный, но дружелюбный. Обращение на «вы»
- Структура блоков:
  1. 🎯 **Для кого этот товар** — целевая аудитория и ситуация использования
  2. ✨ **Ключевые преимущества** — 3-4 главных фишки
  3. 📋 **Характеристики** — текстовым списком без JSON
  4. 🛠️ **Состав / Материалы** — что внутри, из чего сделано
  5. 📦 **Комплектация** — что в коробке
  6. 💡 **Почему выбирают нас** — отстройка от конкурентов
  7. 📐 **Размеры и уход** — размерная сетка, рекомендации по стирке/хранению
- Каждый блок отделяется пустой строкой
- Используй эмодзи, но не более 1 на блок
- Запрещены ссылки, контакты, упоминания других маркетплейсов
- Без HTML-тегов, только текст и эмодзи

### Ключевые слова (wb_keywords)
- Ровно 15 LSI-ключей
- Только ключевые слова и фразы, без чисел и рейтингов
- Включай синонимы и смежные запросы (латинские названия цветов, английские термины)
- Формат: список строк ["ключ 1", "ключ 2", ...]

### Фото-рекомендации (wb_photo_recommendations)
- 5–8 фотографий
- Фото 1: товар на белом фоне, ровный свет, без теней
- Фото 2: инфографика (размеры, материалы, 3-4 ключевых преимущества текстом на фото)
- Фото 3–4: lifestyle (товар в использовании / на модели)
- Фото 5–6: детали (крупный план текстуры, фурнитуры, швов)
- Фото 7: размерная сетка (если применимо)
- Фото 8: сравнение «до/после» или комплектация
- Разрешение: минимум 900×1200 px (вертикальное)
- Формат: JPEG, фон белый #FFFFFF
""".format(version=WB_ALGORITHM_VERSION)


def get_wb_rules() -> str:
    return WB_RULES
